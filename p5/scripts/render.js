function tfswitch(isFalse){
    if(isFalse==false){
        isFalse=true;
    }else{
        isFalse=false;
    }
}

function inBattle(inBattle){
    tfswitch(inBattle);
}
function inPlace(inPlace){
    tfswitch(inPlace);
}
function preBattle(preBattle){
    tfswitch(preBattle);
}
function pause(pause){
    tfswitch(pause);
}